// belajar array
// array di javascript merupakan object khusus yang dapat menyimpan banyak nilai sekaligus

// membuat array
let mahasiswa = ["Nugie kurniawan","Rakha sagraha","Rizal dadang hidayatulloh"]

// mengetahui panjang array menggunakan method length
console.log(mahasiswa.length)

// mencetak satu data array
console.log(mahasiswa[0])

// mencetak seluruh data mahasiswa
for(let i = 0; i<mahasiswa.length;i++){
    console.log(mahasiswa[i])
}